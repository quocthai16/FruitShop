    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        if (session.getAttribute("user") == null) {
            response.sendRedirect("loginpage");
        } else {
            request.getRequestDispatcher("change-password.jsp").forward(request, response);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String pass = request.getParameter("pass");
        String newPass = request.getParameter("newPass");
        String confirmPass = request.getParameter("confirm");
        HttpSession session = request.getSession();
        String username = (String) session.getAttribute("username");
        Users u = new Users();
        Users user = u.getInfoAcount(username);
        if (user.getPassword().equals(u.md5(pass))) {
            u.changePass(username, newPass);
            request.setAttribute("messSuc", "Change password successful!");
        } else {
            request.setAttribute("messFail", "Your password is not correct!");
        }
        request.getRequestDispatcher("change-password.jsp").forward(request, response);

    }

